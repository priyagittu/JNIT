package basicModifier;

public class Operator1 {
public static void main(String[] args) {
		int result=7+1;
	System.out.println("sum of two number is:"+result);
	
	result=result-1;
	System.out.println("subtraction of two number is:"+result);	
	result=result*2;
	System.out.println("multiply of two number is:"+result);	
	result=result/3;
	System.out.println("divided of two number is:"+result);	
	result=result%2;
	System.out.println("modulo of two number is:"+result);
	}

}
